package Entity;

public class users {

	private String users_id;
	private String users_psw;
	private int users_authority;
	private String empl_id;
	public String getUsers_id() {
		return users_id;
	}
	public void setUsers_id(String users_id) {
		this.users_id = users_id;
	}
	public String getUsers_psw() {
		return users_psw;
	}
	public void setUsers_psw(String users_psw) {
		this.users_psw = users_psw;
	}
	public int getUsers_authority() {
		return users_authority;
	}
	public void setUsers_authority(int users_authority) {
		this.users_authority = users_authority;
	}
	public String getEmpl_id() {
		return empl_id;
	}
	public void setEmpl_id(String empl_id) {
		this.empl_id = empl_id;
	}
}
