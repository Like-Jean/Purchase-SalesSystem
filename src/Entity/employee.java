public class employee {
	
	private String empl_id;
	private String empl_name;
	private String empl_department;
	private int empl_position;
	private float empl_salary;
	private String empl_phoneNumber;
	public String getEmpl_id() {
		return empl_id;
	}
	public void setEmpl_id(String empl_id) {
		this.empl_id = empl_id;
	}
	public String getEmpl_name() {
		return empl_name;
	}
	public void setEmpl_name(String empl_name) {
		this.empl_name = empl_name;
	}
	public String getEmpl_department() {
		return empl_department;
	}
	public void setEmpl_department(String empl_department) {
		this.empl_department = empl_department;
	}
	public int getEmpl_position() {
		return empl_position;
	}
	public void setEmpl_position(int empl_position) {
		this.empl_position = empl_position;
	}
	public float getEmpl_salary() {
		return empl_salary;
	}
	public void setEmpl_salary(float empl_salary) {
		this.empl_salary = empl_salary;
	}
	public String getEmpl_phoneNumber() {
		return empl_phoneNumber;
	}
	public void setEmpl_phoneNumber(String empl_phoneNumber) {
		this.empl_phoneNumber = empl_phoneNumber;
	}

}
